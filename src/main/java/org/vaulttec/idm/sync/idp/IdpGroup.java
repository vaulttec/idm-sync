package org.vaulttec.idm.sync.idp;

import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown = true)
public class IdpGroup {

  private String id;
  private String name;
  private String path;
  private Map<String, List<String>> attributes = new HashMap<>();
  private List<IdpUser> members = new ArrayList<>();

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getPath() {
    return path;
  }

  public void setPath(String path) {
    this.path = path;
  }

  public Map<String, List<String>> getAttributes() {
    return attributes;
  }

  public String getAttribute(String name) {
    List<String> values = attributes.get(name);
    return values != null && !values.isEmpty() ? values.get(0) : null;
  }

  public void setAttributes(Map<String, List<String>> attributes) {
    this.attributes = attributes;
  }

  public List<IdpUser> getMembers() {
    return members;
  }

  public void addMember(IdpUser member) {
    this.members.add(member);
  }

  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = prime * result + ((path == null) ? 0 : path.hashCode());
    return result;
  }

  @Override
  public boolean equals(Object obj) {
    if (this == obj) {
      return true;
    }
    if (obj == null) {
      return false;
    }
    if (getClass() != obj.getClass()) {
      return false;
    }
    IdpGroup other = (IdpGroup) obj;
    if (path == null) {
      if (other.path != null) {
        return false;
      }
    } else if (!path.equals(other.path)) {
      return false;
    }
    return true;
  }

  @Override
  public String toString() {
    return path + "(" + members.size() + ")";
  }
}
