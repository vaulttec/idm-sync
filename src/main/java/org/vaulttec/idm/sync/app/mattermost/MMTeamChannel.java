package org.vaulttec.idm.sync.app.mattermost;

import java.util.Date;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
public class MMTeamChannel {

  private String id;
  private String name;
  @JsonProperty("last_post_at")
  private Date lastPostAt;
  @JsonProperty("total_msg_count")
  private int messageCount;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public Date getLastPostAt() {
    return lastPostAt;
  }

  public void setLastPostAt(Date lastPostAt) {
    this.lastPostAt = lastPostAt;
  }

  public int getMessageCount() {
    return messageCount;
  }

  public void setMessageCount(int messageCount) {
    this.messageCount = messageCount;
  }

  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = prime * result + ((id == null) ? 0 : id.hashCode());
    return result;
  }

  @Override
  public boolean equals(Object obj) {
    if (this == obj) {
      return true;
    }
    if (obj == null) {
      return false;
    }
    if (getClass() != obj.getClass()) {
      return false;
    }
    MMTeamChannel other = (MMTeamChannel) obj;
    if (id == null) {
      if (other.id!= null) {
        return false;
      }
    } else if (!id.equals(other.id)) {
      return false;
    }
    return true;
  }

  @Override
  public String toString() {
    return name + "(" + messageCount + ")";
  }
}
