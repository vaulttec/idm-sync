package org.vaulttec.idm.sync.app.mattermost;

import java.util.Collection;
import java.util.HashMap;
import java.util.Map;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;

@JsonIgnoreProperties(ignoreUnknown = true)
public class MMTeam {

  private String id;
  private String name;
  private String type;
  private Map<String, MMUser> members = new HashMap<>();
  private Map<String, MMRole> memberRoles = new HashMap<>();

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  public boolean hasMember(MMUser user) {
    return this.members.containsKey(user.getUsername());
  }

  public MMUser getMember(String username) {
    return members.get(username);
  }

  public MMRole getMemberRole(String username) {
    return memberRoles.get(username);
  }

  public Collection<MMUser> getMembers() {
    return members.values();
  }

  public void addMember(MMUser member, MMRole role) {
    members.put(member.getUsername(), member);
    memberRoles.put(member.getUsername(), role);
  }

  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = prime * result + ((name == null) ? 0 : name.hashCode());
    return result;
  }

  @Override
  public boolean equals(Object obj) {
    if (this == obj) {
      return true;
    }
    if (obj == null) {
      return false;
    }
    if (getClass() != obj.getClass()) {
      return false;
    }
    MMTeam other = (MMTeam) obj;
    if (name == null) {
      if (other.name != null) {
        return false;
      }
    } else if (!name.equals(other.name)) {
      return false;
    }
    return true;
  }

  @Override
  public String toString() {
    return name + "(" + members.size() + ")";
  }
}
